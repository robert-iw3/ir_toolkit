<#
.SYNOPSIS
    Pester 5 tests for IR Toolkit code signing (Invoke-SignToolkit.ps1).
    Phase 1 (unit): validates the signing script structure and cert logic.
    Phase 2 (live): verifies signatures after Invoke-SignToolkit.ps1 has been run.
#>

BeforeAll {
    $Script:ToolkitRoot = Join-Path $PSScriptRoot '..\..\'
    $Script:ToolsDir    = Join-Path $Script:ToolkitRoot 'tools'
    $Script:SignScript  = Join-Path $Script:ToolkitRoot 'Invoke-SignToolkit.ps1'
    $Script:PrepScript  = Join-Path $Script:ToolkitRoot 'Invoke-PrepareDefender.ps1'
    $Script:CerPath     = Join-Path $Script:ToolsDir 'ir_toolkit.cer'
    $Script:PfxPath     = Join-Path $Script:ToolsDir 'ir_toolkit.pfx'
}

Describe "Invoke-SignToolkit.ps1 — script structure" {

    It "Signing script exists" {
        Test-Path -LiteralPath $Script:SignScript | Should -Be $true
    }

    It "Signing script parses without errors" {
        $r = pwsh -NoProfile -Command "[scriptblock]::Create((Get-Content -Raw '$($Script:SignScript)'))" 2>&1
        $r | Where-Object { $_ -is [System.Management.Automation.ErrorRecord] } | Should -BeNullOrEmpty
    }

    It "Signing script requires RunAsAdministrator" {
        $content = Get-Content -LiteralPath $Script:SignScript -Raw
        $content | Should -Match '#Requires -RunAsAdministrator'
    }

    It "Signing script exports both .cer and .pfx" {
        $content = Get-Content -LiteralPath $Script:SignScript -Raw
        $content | Should -Match 'Export-Certificate'
        $content | Should -Match 'Export-PfxCertificate'
    }

    It "Signing script adds cert to TrustedPublisher store" {
        $content = Get-Content -LiteralPath $Script:SignScript -Raw
        $content | Should -Match 'TrustedPublisher'
    }

    It "Signing script calls Set-AuthenticodeSignature on all .ps1 files" {
        $content = Get-Content -LiteralPath $Script:SignScript -Raw
        $content | Should -Match 'Set-AuthenticodeSignature'
        $content | Should -Match '\*\.ps1'
    }

    It "Signing script includes DigiCert timestamp server URL" {
        $content = Get-Content -LiteralPath $Script:SignScript -Raw
        $content | Should -Match 'timestamp\.digicert\.com'
    }
}

Describe "Invoke-PrepareDefender.ps1 — cert import logic" {

    It "PrepareDefender script imports cert from tools\ir_toolkit.cer if present" {
        $content = Get-Content -LiteralPath $Script:PrepScript -Raw
        $content | Should -Match 'ir_toolkit\.cer'
        $content | Should -Match 'TrustedPublisher'
        $content | Should -Match 'X509Certificate2'
    }

    It "PrepareDefender logs a message when cert is not present" {
        $content = Get-Content -LiteralPath $Script:PrepScript -Raw
        $content | Should -Match 'Invoke-SignToolkit'
    }
}

Describe "Code signing — live verification (requires Invoke-SignToolkit.ps1 to have been run)" {

    It "ir_toolkit.cer exists after signing" -Skip:(-not (Test-Path $Script:CerPath)) {
        Test-Path -LiteralPath $Script:CerPath | Should -Be $true
    }

    It "ir_toolkit.pfx exists on analyst machine" -Skip:(-not (Test-Path $Script:PfxPath)) {
        Test-Path -LiteralPath $Script:PfxPath | Should -Be $true
    }

    It "Invoke-IRCollection.ps1 has a signature block" -Skip:(-not (Test-Path $Script:CerPath)) {
        # After Invoke-SignToolkit.ps1, cert is removed from local stores (no trace).
        # Status is 'UnknownError' (chain unverifiable) — NOT 'NotSigned' (no block at all).
        $target = Join-Path $Script:ToolkitRoot 'Invoke-IRCollection.ps1'
        $sig    = Get-AuthenticodeSignature -LiteralPath $target -ErrorAction SilentlyContinue
        $sig.Status | Should -Not -Be 'NotSigned'
        $sig.SignerCertificate.Subject | Should -Match 'IR Toolkit'
    }

    It "Invoke-PrepareDefender.ps1 has a signature block" -Skip:(-not (Test-Path $Script:CerPath)) {
        $sig = Get-AuthenticodeSignature -LiteralPath $Script:PrepScript -ErrorAction SilentlyContinue
        $sig.Status | Should -Not -Be 'NotSigned'
        $sig.SignerCertificate.Subject | Should -Match 'IR Toolkit'
    }

    It "All .ps1 files under playbooks\ have a signature block" -Skip:(-not (Test-Path $Script:CerPath)) {
        $unsigned = Get-ChildItem (Join-Path $Script:ToolkitRoot 'playbooks') -Recurse -Filter '*.ps1' |
            Where-Object {
                $s = Get-AuthenticodeSignature -LiteralPath $_.FullName -ErrorAction SilentlyContinue
                $s.Status -eq 'NotSigned'
            }
        $unsigned | Should -BeNullOrEmpty -Because 'All playbooks scripts must have a signature block'
    }
}

