<#
.SYNOPSIS
    Pester 5 tests for memory-findings JSON I/O in Analyze-Memory.ps1.

    Regression: under Windows PowerShell 5.1, `@(Get-Content '[]' | ConvertFrom-Json)`
    yields a bogus count of 1 (5.1 emits the empty array as one pipeline object), and
    `$findings | ConvertTo-Json` serialises a List/array into a {value,Count} wrapper or
    collapses a single finding to a bare object. On the GOTEM AFF4 run this surfaced as
    "1 concerning finding" with an empty {value:[],Count:0} JSON file.

    These tests pin two extracted helpers so the behaviour holds on BOTH 5.1 and 7+:
      Import-MemoryFindings  - read a findings JSON file -> normalized array (true count)
      ConvertTo-FindingsJson - serialize findings -> ALWAYS a JSON array string
#>

BeforeAll {
    $Script:MemScript = Join-Path $PSScriptRoot '..\..\playbooks\windows\threat_hunting\Analyze-Memory.ps1'
    $tokens = $null; $errors = $null
    $ast = [System.Management.Automation.Language.Parser]::ParseFile(
        $Script:MemScript, [ref]$tokens, [ref]$errors)

    foreach ($name in 'Import-MemoryFindings','ConvertTo-FindingsJson') {
        $fn = $ast.FindAll(
            { param($n) $n -is [System.Management.Automation.Language.FunctionDefinitionAst] -and
                        $n.Name -eq $name }, $true) | Select-Object -First 1
        if ($fn) { . ([scriptblock]::Create($fn.Extent.Text)) }
    }
}

Describe "Import-MemoryFindings — true count (PS 5.1 empty-array quirk)" {

    It "Defines the helper" {
        (Get-Command Import-MemoryFindings -ErrorAction SilentlyContinue) | Should -Not -BeNullOrEmpty
    }

    # Contract: helper returns a List, so callers use .Count directly (no @() wrap).
    It "Empty array '[]' -> count 0 (not 1)" {
        $p = Join-Path $TestDrive 'empty.json'; '[]' | Out-File $p -Encoding UTF8
        (Import-MemoryFindings -Path $p).Count | Should -Be 0
    }

    It "Single finding -> count 1" {
        $p = Join-Path $TestDrive 'one.json'
        '[{"Severity":"High","Type":"YARA Match (Memory)"}]' | Out-File $p -Encoding UTF8
        (Import-MemoryFindings -Path $p).Count | Should -Be 1
    }

    It "Three findings -> count 3" {
        $p = Join-Path $TestDrive 'three.json'
        '[{"a":1},{"a":2},{"a":3}]' | Out-File $p -Encoding UTF8
        (Import-MemoryFindings -Path $p).Count | Should -Be 3
    }

    It "Missing file -> count 0 (no throw)" {
        (Import-MemoryFindings -Path (Join-Path $TestDrive 'nope.json')).Count | Should -Be 0
    }

    It "Empty / whitespace file -> count 0 (no throw)" {
        $p = Join-Path $TestDrive 'blank.json'; '   ' | Out-File $p -Encoding UTF8
        (Import-MemoryFindings -Path $p).Count | Should -Be 0
    }

    It "Preserves finding fields" {
        $p = Join-Path $TestDrive 'fields.json'
        '[{"Severity":"Critical","Type":"YARA Match (Memory)","Target":"PID 4 (System)"}]' |
            Out-File $p -Encoding UTF8
        $f = Import-MemoryFindings -Path $p
        $f[0].Severity | Should -Be 'Critical'
        $f[0].Target   | Should -Be 'PID 4 (System)'
    }
}

Describe "ConvertTo-FindingsJson — always a JSON array" {

    It "Defines the helper" {
        (Get-Command ConvertTo-FindingsJson -ErrorAction SilentlyContinue) | Should -Not -BeNullOrEmpty
    }

    It "Empty -> '[]'" {
        (ConvertTo-FindingsJson @()).Trim() | Should -Be '[]'
    }

    It "Single finding serializes as a 1-element ARRAY (not a bare object)" {
        $one  = [pscustomobject]@{ Severity='High'; Type='X' }
        $json = ConvertTo-FindingsJson @($one)
        $json.TrimStart()[0] | Should -Be '['
        @($json | ConvertFrom-Json).Count | Should -Be 1
    }

    It "Many findings round-trip to the same count" {
        $many = 1..4 | ForEach-Object { [pscustomobject]@{ Severity='High'; Type="T$_" } }
        $json = ConvertTo-FindingsJson $many
        @($json | ConvertFrom-Json).Count | Should -Be 4
    }

    It "Never emits the {value,Count} List wrapper" {
        $json = ConvertTo-FindingsJson @([pscustomobject]@{ Severity='High' })
        $json | Should -Not -Match '"value"\s*:'
        $json | Should -Not -Match '"Count"\s*:'
    }
}

Describe "Analyze-Memory output path — regression guards" {

    It "Does not use the fragile '@() | ConvertTo-Json' empty-write pattern" {
        $raw = Get-Content -LiteralPath $Script:MemScript -Raw
        $raw | Should -Not -Match '@\(\)\s*\|\s*ConvertTo-Json'
    }

    It "Uses ConvertTo-FindingsJson for the findings output write" {
        $raw = Get-Content -LiteralPath $Script:MemScript -Raw
        $raw | Should -Match 'ConvertTo-FindingsJson'
    }
}

