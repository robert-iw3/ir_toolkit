<#
.SYNOPSIS
    Pester 5 tests for YARA support in the Volatility branch of Analyze-Memory.ps1.

    Parity goal: the Volatility/raw path classifies rule names the same way the
    MemProcFS/Python path (memory_yara.py) does, and builds a single combined
    include-file for vol3 windows.vadyarascan --yara-file (mirrors the Linux
    analyze_memory_linux.resolve_yara pattern).

    Helpers under test (extracted from the script via AST, no image needed):
      Test-YaraNoiseRule   - noise-prefix suppression  (== memory_yara.is_noise_rule)
      Get-YaraSeverity     - Critical/High classifier   (== memory_yara.severity_for_rule)
      New-CombinedYaraFile - build the vol3 include file from a rules dir
#>

BeforeAll {
    $Script:MemScript = Join-Path $PSScriptRoot '..\..\playbooks\windows\threat_hunting\Analyze-Memory.ps1'
    $tokens = $null; $errors = $null
    $ast = [System.Management.Automation.Language.Parser]::ParseFile(
        $Script:MemScript, [ref]$tokens, [ref]$errors)
    foreach ($name in 'Test-YaraNoiseRule','Get-YaraSeverity','New-CombinedYaraFile') {
        $fn = $ast.FindAll(
            { param($n) $n -is [System.Management.Automation.Language.FunctionDefinitionAst] -and
                        $n.Name -eq $name }, $true) | Select-Object -First 1
        if ($fn) { . ([scriptblock]::Create($fn.Extent.Text)) }
    }
}

Describe "Test-YaraNoiseRule — parity with memory_yara.is_noise_rule" {
    It "Suppresses noise prefix <N>" -ForEach @(
        @{N='generic_test'}, @{N='PUA_thing'}, @{N='with_suffix'}, @{N='debug_x'}
    ) { Test-YaraNoiseRule $N | Should -BeTrue }

    It "Keeps real rule <N>" -ForEach @(
        @{N='Cobalt_Strike_Beacon'}, @{N='Mimikatz'}, @{N='APT_Implant'}
    ) { Test-YaraNoiseRule $N | Should -BeFalse }
}

Describe "Get-YaraSeverity — parity with memory_yara.severity_for_rule" {
    It "Critical for high-signal <N>" -ForEach @(
        @{N='CobaltStrike_x'}, @{N='win_meterpreter'}, @{N='generic_shellcode'},
        @{N='Mimikatz_creds'}, @{N='process_inject'}
    ) { Get-YaraSeverity $N | Should -Be 'Critical' }

    It "High for ordinary rule names" {
        Get-YaraSeverity 'Suspicious_Office_Macro' | Should -Be 'High'
    }
}

Describe "New-CombinedYaraFile — vol3 --yara-file include builder" {
    It "Builds an include file referencing every staged .yar/.yara" {
        $dir = Join-Path $TestDrive 'rules'
        New-Item -ItemType Directory -Force (Join-Path $dir 'sub') | Out-Null
        'rule A { condition: true }'  | Out-File (Join-Path $dir 'a.yar')        -Encoding UTF8
        'rule B { condition: false }' | Out-File (Join-Path $dir 'sub\b.yara')   -Encoding UTF8

        $combined = New-CombinedYaraFile -RulesDir $dir
        Test-Path -LiteralPath $combined | Should -BeTrue
        $lines = Get-Content -LiteralPath $combined | Where-Object { $_ -match '^\s*include ' }
        $lines.Count | Should -Be 2
        ($lines -join "`n") | Should -Match 'a\.yar'
        ($lines -join "`n") | Should -Match 'b\.yara'
    }

    It "Returns null for a missing rules dir" {
        New-CombinedYaraFile -RulesDir (Join-Path $TestDrive 'nope') | Should -BeNullOrEmpty
    }

    It "Returns null for an empty rules dir" {
        $empty = Join-Path $TestDrive 'empty'; New-Item -ItemType Directory -Force $empty | Out-Null
        New-CombinedYaraFile -RulesDir $empty | Should -BeNullOrEmpty
    }
}

Describe "Analyze-Memory Volatility branch — wiring" {
    It "Runs windows.vadyarascan in the Volatility path" {
        (Get-Content -LiteralPath $Script:MemScript -Raw) | Should -Match 'windows\.vadyarascan'
    }
}

