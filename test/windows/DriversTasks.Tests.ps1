BeforeAll {
    $SrcPath = Join-Path $PSScriptRoot "..\..\playbooks\windows\threat_hunting\dev\src"
    . (Join-Path $SrcPath "00_Parameters_And_Globals.ps1")
    . (Join-Path $SrcPath "04_Drivers_And_Tasks.ps1")
}

Describe "Driver and Task Hunt Modules" {

    BeforeEach {
        $script:Findings = @()
        $script:Quiet = $true   # consumed by Write-Console in sourced functions
    }

    It "Should detect a known vulnerable kernel driver (BYOVD)" {
        $AutoUpdateDrivers = $false
        Mock Get-CimInstance {
            @( [PSCustomObject]@{ Name = "RTCore64.sys"; DisplayName = "Micro-Star MSI Afterburner"; PathName = "C:\Windows\System32\drivers\RTCore64.sys" } )
        } -ParameterFilter { $ClassName -eq 'Win32_SystemDriver' }
        Mock Test-Path { $true }
        Mock Get-AuthenticodeSignature { [PSCustomObject]@{ Status = "Valid" } }
        Mock Get-FileHash { [PSCustomObject]@{ Hash = 'AAAA1111' * 8 } }

        Invoke-DriverHunt

        $Alert = $script:Findings | Where-Object { $_.Type -eq "Suspicious Kernel Driver" }
        $Alert | Should -Not -BeNullOrEmpty
        $Alert.Target | Should -Match "RTCore64"
        $Alert.Severity | Should -Be "High"
    }

    It "Should detect a Scheduled Task executing a LOLBin (mshta)" {
        # mshta scores 3 on its own (threshold = 3), so no additional indicators needed.
        # powershell.exe -WindowStyle Hidden only scores 1 — below threshold.
        Mock Get-ScheduledTask {
            return @(
                [PSCustomObject]@{
                    TaskName = "PersistenceTask"
                    TaskPath = "\"
                    State    = "Ready"
                    Actions  = @( [PSCustomObject]@{ Execute = "mshta.exe"; Arguments = "vbscript:window.close" } )
                    Triggers = @( [PSCustomObject]@{ GetType = { return [PSCustomObject]@{ Name = "LogonTrigger" } } } )
                }
            )
        }

        Invoke-ScheduledTaskHunt

        $Alert = $script:Findings | Where-Object { $_.Type -eq "Suspicious Scheduled Task" }
        $Alert | Should -Not -BeNullOrEmpty
        $Alert.Details | Should -Match "mshta.exe"
    }
}

