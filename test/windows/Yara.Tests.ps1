<#
.SYNOPSIS
    Pester 5 tests for Invoke-YaraFileScan (targeted YARA integration).
#>

BeforeAll {
    $SrcPath = Join-Path $PSScriptRoot "..\..\playbooks\windows\threat_hunting\dev\src"
    . (Join-Path $SrcPath "00_Parameters_And_Globals.ps1")
    . (Join-Path $SrcPath "05_File_And_ADS_Hunt.ps1")
}

Describe "Invoke-YaraFileScan Module" {

    BeforeEach {
        $script:Findings = @()
    }

    It "Should skip gracefully when yara64.exe is not staged" {
        # No tools\ folder with yara64.exe will be found from the test src path,
        # so the function must warn and return without throwing.
        { Invoke-YaraFileScan -TargetPath $TestDrive -Quiet:$true } | Should -Not -Throw
        $script:Findings.Count | Should -Be 0
    }

    It "Should parse yara64 output and route matches as High findings" {
        # Simulate yara64.exe returning one match line
        $fakeBin = Join-Path $TestDrive "yara64.exe"
        Set-Content $fakeBin "dummy"

        $fakeRules = Join-Path $TestDrive "rules"
        New-Item -ItemType Directory $fakeRules -Force | Out-Null
        Set-Content (Join-Path $fakeRules "test.yar") "rule Test {}"

        $fakeTarget = Join-Path $TestDrive "scan_target"
        New-Item -ItemType Directory $fakeTarget -Force | Out-Null
        $suspiciousFile = Join-Path $fakeTarget "payload.exe"
        Set-Content $suspiciousFile "MZ"

        # Pre-seed a finding so the targeted path logic has a file to resolve
        $script:Findings = @([PSCustomObject]@{
            Type="High Entropy File"; Target=$suspiciousFile
            Severity="High"; Details="Entropy: 7.9"; MITRE="T1027"; Timestamp=(Get-Date).ToString()
        })

        # Mock the yara64 invocation to return a synthetic match line
        Mock -CommandName Get-ChildItem -MockWith {
            return @([System.IO.FileInfo]::new($fakeRules + "\test.yar"))
        } -ParameterFilter { $Filter -eq '*.yar*' -or $Filter -eq '*.yar' }

        # Mock the native call (&) — capture the scriptblock so we can inject output
        # The function calls:  & $yaraExe @yaraArgs
        # We test the Add-Finding pathway directly instead, since mocking native binaries
        # in Pester requires the binary to exist. Verify the finding schema here.
        Add-Finding -Type "YARA Match" -Target $suspiciousFile `
            -Details "Rule: Malware_Generic" -Severity "High" `
            -Mitre "T1027 (Obfuscated Files or Information)"

        $Alert = $script:Findings | Where-Object { $_.Type -eq "YARA Match" }
        $Alert | Should -Not -BeNullOrEmpty
        $Alert.Severity | Should -Be "High"
        $Alert.Details  | Should -Match "Malware_Generic"
    }

    # The rule set must NOT be gated on prior finding types (a host can be
    # compromised with zero prior file findings), and must include the elastic
    # Windows_Trojan APT rules. These guard the 2026-06-21 coverage fix.
    Context "Rule coverage + trust (no gating, canary, error surfacing)" {
        BeforeAll {
            $Script:FileHuntSrc = Get-Content -LiteralPath (Join-Path $SrcPath '05_File_And_ADS_Hunt.ps1') -Raw
        }

        It "Does not gate the rule set on prior finding types" {
            $Script:FileHuntSrc | Should -Not -Match '\$typeRuleMap'
        }

        It "Recurses the full rules directory (covers elastic/neo23x0/reversinglabs)" {
            $Script:FileHuntSrc | Should -Match "Get-ChildItem \`$RulesDir -Recurse -Include '\*\.yar','\*\.yara'"
        }

        It "Runs a YARA self-test canary" {
            $Script:FileHuntSrc | Should -Match 'IRToolkit_File_Canary'
            $Script:FileHuntSrc | Should -Match 'self-test'
        }

        It "Surfaces yara64 errors instead of discarding them (no 2>\$null on the scan)" {
            # the scan now redirects stderr to a temp file it inspects
            $Script:FileHuntSrc | Should -Match '2>\$errFile'
        }

        It "Excludes the abuse.ch feed (dup rule-ids + PE-structure noise)" {
            $Script:FileHuntSrc | Should -Match 'noiseFeed'
            $Script:FileHuntSrc | Should -Match 'abusech'
        }
    }

    Context "abuse.ch exclusion regex" {
        BeforeAll { $Script:NoiseFeed = '(?i)[\\/]abusech[\\/]' }
        It "Excludes <P>" -ForEach @(
            @{P='C:\t\yara_rules\abusech\Luckyware.yar'}, @{P='/t/yara_rules/abusech/x.yar'}
        ) { ($P -match $Script:NoiseFeed) | Should -BeTrue }
        It "Keeps <P>" -ForEach @(
            @{P='C:\t\yara_rules\elastic\Windows_Trojan_REDLEAVES.yar'}, @{P='/t/yara_rules/neo23x0/gen.yar'}
        ) { ($P -match $Script:NoiseFeed) | Should -BeFalse }
    }

    Context "Windows rule filter" {
        # Mirror the inline $nonWin pattern; Linux/macOS dropped, Windows/generic kept.
        BeforeAll {
            $Script:NonWin = '(?i)(?:^|[\\/_])(linux|macos|osx|android|freebsd|ios)(?:[\\/_.]|$)'
        }
        It "Drops <N>" -ForEach @(@{N='Linux_Trojan_X.yar'}, @{N='MacOS_Y.yara'}, @{N='macos_z.yar'}) {
            ($N -match $Script:NonWin) | Should -BeTrue
        }
        It "Keeps <N>" -ForEach @(@{N='Windows_Trojan_REDLEAVES.yar'}, @{N='gen_mimikatz.yar'}, @{N='Multi_Generic.yar'}) {
            ($N -match $Script:NonWin) | Should -BeFalse
        }
    }
}

