<#
.SYNOPSIS
    Pester 5 tests for playbooks/windows/01-06 workflow alignment.
    Validates parameter consistency, dry-run guards, and path parameterisation.
#>

BeforeAll {
    $Script:PlaybookDir = Join-Path $PSScriptRoot '..\..\playbooks\windows'
    $Script:Scripts = @{
        '01' = Join-Path $Script:PlaybookDir '01_Contain-Host.ps1'
        '02' = Join-Path $Script:PlaybookDir '02_Eradicate-Process.ps1'
        '03' = Join-Path $Script:PlaybookDir '03_Eradicate-Persistence.ps1'
        '04' = Join-Path $Script:PlaybookDir '04_Block-C2.ps1'
        '05' = Join-Path $Script:PlaybookDir '05_Acquire-Artifact.ps1'
        '06' = Join-Path $Script:PlaybookDir '06_Restore-Host.ps1'
    }
}

Describe "Playbook scripts 01-06 — parse and structure" {

    It "<name> parses without errors" -ForEach @(
        @{name='01_Contain-Host';           n='01'}
        @{name='02_Eradicate-Process';      n='02'}
        @{name='03_Eradicate-Persistence';  n='03'}
        @{name='04_Block-C2';               n='04'}
        @{name='05_Acquire-Artifact';       n='05'}
        @{name='06_Restore-Host';           n='06'}
    ) {
        $path = $Script:Scripts[$n]
        Test-Path -LiteralPath $path | Should -Be $true
        $r = pwsh -NoProfile -Command "[scriptblock]::Create((Get-Content -Raw '$path'))" 2>&1
        $r | Where-Object { $_ -is [System.Management.Automation.ErrorRecord] } | Should -BeNullOrEmpty
    }

    It "<name> has -OutputDir parameter" -ForEach @(
        @{name='01_Contain-Host';    n='01'}
        @{name='02_Eradicate-Proc';  n='02'}
        @{name='03_Eradicate-Pers';  n='03'}
        @{name='04_Block-C2';        n='04'}
        @{name='05_Acquire';         n='05'}
        @{name='06_Restore-Host';    n='06'}
    ) {
        $content = Get-Content -LiteralPath $Script:Scripts[$n] -Raw
        $content | Should -Match '\$OutputDir'
    }

    It "<name> has -IncidentId parameter" -ForEach @(
        @{name='01_Contain-Host';    n='01'}
        @{name='02_Eradicate-Proc';  n='02'}
        @{name='03_Eradicate-Pers';  n='03'}
        @{name='04_Block-C2';        n='04'}
        @{name='06_Restore-Host';    n='06'}
    ) {
        $content = Get-Content -LiteralPath $Script:Scripts[$n] -Raw
        $content | Should -Match '\$IncidentId'
    }

    It "<name> uses OutputDir over hardcoded C:\\ProgramData\\IRToolkit" -ForEach @(
        @{name='01_Contain-Host';    n='01'}
        @{name='02_Eradicate-Proc';  n='02'}
        @{name='03_Eradicate-Pers';  n='03'}
        @{name='04_Block-C2';        n='04'}
        @{name='06_Restore-Host';    n='06'}
    ) {
        $content = Get-Content -LiteralPath $Script:Scripts[$n] -Raw
        $content | Should -Match 'if.*\$OutputDir.*ProgramData'
    }

    It "<name> does not use ErrorActionPreference Stop (resilient standard)" -ForEach @(
        @{name='01'; n='01'}, @{name='02'; n='02'}, @{name='03'; n='03'},
        @{name='04'; n='04'}, @{name='05'; n='05'}, @{name='06'; n='06'}
    ) {
        $content = Get-Content -LiteralPath $Script:Scripts[$n] -Raw
        # Remove comments so we only check live code
        $live = ($content -split "`n" | Where-Object { $_ -notmatch '^\s*#' }) -join "`n"
        $live | Should -Not -Match "ErrorActionPreference\s*=\s*'Stop'"
    }
}

Describe "Playbook 02 and 03 — dry-run guard" {

    It "02_Eradicate-Process.ps1 has -Apply parameter and uses it as gate" {
        $content = Get-Content -LiteralPath $Script:Scripts['02'] -Raw
        $content | Should -Match '\[switch\]\$Apply'
        $content | Should -Match 'if.*\$Apply'
    }

    It "03_Eradicate-Persistence.ps1 has -Apply parameter and mode guard" {
        $content = Get-Content -LiteralPath $Script:Scripts['03'] -Raw
        $content | Should -Match '\[switch\]\$Apply'
        $content | Should -Match '\$mode'
    }
}

