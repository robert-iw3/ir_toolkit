<#
.SYNOPSIS
    Pester 5 tests for Seal-EvidenceCustody.ps1 (PS twin of evidence_custody.py).
    Unit: seal, verify, tamper detection. Live: check existing reports/.
#>

BeforeAll {
    $Script:SealScript = Join-Path $PSScriptRoot '..\..\playbooks\windows\Seal-EvidenceCustody.ps1'
    $Script:ReportsDir = Join-Path $PSScriptRoot '..\..\reports'
}

Describe "Seal-EvidenceCustody.ps1 — structure" {

    It "Script file exists" {
        Test-Path -LiteralPath $Script:SealScript | Should -Be $true
    }

    It "Script parses without errors" {
        $r = pwsh -NoProfile -Command "[scriptblock]::Create((Get-Content -Raw '$Script:SealScript'))" 2>&1
        $r | Where-Object { $_ -is [System.Management.Automation.ErrorRecord] } | Should -BeNullOrEmpty
    }

    It "Has -HostFolder, -Verify, and -IncidentId parameters" {
        $c = Get-Content -LiteralPath $Script:SealScript -Raw
        $c | Should -Match '\$HostFolder'
        $c | Should -Match '\$Verify'
        $c | Should -Match '\$IncidentId'
    }

    It "Invoke-IRCollection.ps1 calls Seal-EvidenceCustody.ps1" {
        $orc = Join-Path $PSScriptRoot '..\..\Invoke-IRCollection.ps1'
        $c   = Get-Content -LiteralPath $orc -Raw
        $c   | Should -Match 'Seal-EvidenceCustody'
        $c   | Should -Match 'SealScript'
    }
}

Describe "Seal-EvidenceCustody.ps1 — unit tests" {

    It "Creates _custody_*.json when manifest exists" {
        $dir = Join-Path $TestDrive 'custody_test'
        New-Item -ItemType Directory $dir -Force | Out-Null
        # Create a synthetic manifest
        '{"incident_id":"TEST","artifacts":[]}' | Out-File (Join-Path $dir '_manifest_20260621_000000.json') -Encoding UTF8

        & $Script:SealScript -HostFolder $dir -IncidentId 'TEST' -Quiet
        $custody = Get-ChildItem $dir -Filter '_custody_*.json'
        $custody | Should -Not -BeNullOrEmpty
    }

    It "_custody_*.json has required schema fields" {
        $dir = Join-Path $TestDrive 'custody_schema'
        New-Item -ItemType Directory $dir -Force | Out-Null
        '{"test":1}' | Out-File (Join-Path $dir '_manifest_20260621_000000.json') -Encoding UTF8

        & $Script:SealScript -HostFolder $dir -Quiet
        $cf   = Get-ChildItem $dir -Filter '_custody_*.json' | Select-Object -First 1
        $data = Get-Content $cf.FullName -Raw | ConvertFrom-Json

        $data.schema         | Should -Be 'ir-toolkit/custody/1.0'
        $data.manifest_file  | Should -Match '_manifest_'
        $data.manifest_sha256 | Should -Match '^[0-9a-f]{64}$'
        $data.operator       | Should -Not -BeNullOrEmpty
        $data.collected_utc  | Should -Not -BeNullOrEmpty
    }

    It "Appends to _custody_log.jsonl" {
        $dir = Join-Path $TestDrive 'custody_log'
        New-Item -ItemType Directory $dir -Force | Out-Null
        '{"a":1}' | Out-File (Join-Path $dir '_manifest_20260621_000000.json') -Encoding UTF8

        & $Script:SealScript -HostFolder $dir -Quiet
        $log = Join-Path $dir '_custody_log.jsonl'
        Test-Path $log | Should -Be $true
        (Get-Content $log | Measure-Object -Line).Lines | Should -BeGreaterOrEqual 1
    }

    It "-Verify returns 0 when manifest is unmodified" {
        $dir = Join-Path $TestDrive 'custody_verify'
        New-Item -ItemType Directory $dir -Force | Out-Null
        '{"verify":true}' | Out-File (Join-Path $dir '_manifest_20260621_000000.json') -Encoding UTF8

        & $Script:SealScript -HostFolder $dir -Quiet
        $exit = pwsh -NoProfile -ExecutionPolicy Bypass -Command "
            & '$($Script:SealScript)' -HostFolder '$dir' -Verify -Quiet
            exit `$LASTEXITCODE
        " 2>&1
        $LASTEXITCODE | Should -Be 0
    }

    It "-Verify returns 1 when manifest is tampered" {
        $dir = Join-Path $TestDrive 'custody_tamper'
        New-Item -ItemType Directory $dir -Force | Out-Null
        $mf = Join-Path $dir '_manifest_20260621_000000.json'
        '{"original":true}' | Out-File $mf -Encoding UTF8

        & $Script:SealScript -HostFolder $dir -Quiet
        # Tamper the manifest
        '{"tampered":true}' | Out-File $mf -Encoding UTF8 -Force

        $null = pwsh -NoProfile -ExecutionPolicy Bypass -Command "
            & '$($Script:SealScript)' -HostFolder '$dir' -Verify -Quiet
            exit `$LASTEXITCODE
        " 2>&1
        $LASTEXITCODE | Should -Be 1
    }
}

Describe "Seal-EvidenceCustody.ps1 — live validation" -Skip:(-not (Get-ChildItem $Script:ReportsDir -Recurse -Filter '_custody_*.json' -ErrorAction SilentlyContinue)) {

    It "_custody_*.json exists in at least one report directory" {
        Get-ChildItem $Script:ReportsDir -Recurse -Filter '_custody_*.json' -ErrorAction SilentlyContinue |
            Should -Not -BeNullOrEmpty
    }
}

